import numpy as np # for doing stuff with numbers
import matplotlib.pyplot as plt # for plotting
import pandas as pd # to easily print matrices

##########################################
# 1. define parameters for a qw on a line
##########################################

# test different gamma_2s and taus (2D GRID) for fixed size N
num_sites = 20
target_site = num_sites // 2 # opposite end
num_gamma2_points = 230 # horizontal resolution
num_tau_points = 230 # vertical resolution
gamma2_min = 0.0
gamma2_max = 1.0
tau_min = 0.02
tau_max = 4.00

phi_1 = 0.0
M = 2000

# Load the matrix generated by the C++ simulation engine
mean_hitting_times = np.loadtxt(f'RESULTS_mean_hitting_time_PVM_phi1_{phi_1:.6f}_N_{num_sites}_resolution_{num_gamma2_points}x{num_tau_points}_{M}_runs.txt')

##########################################
# parts 2. 3. 4. are in the C++ code
##########################################

##########################################
# 5. plot of the 2d mean hitting time grid
##########################################

plt.figure(figsize=(8,6))
extent = [gamma2_min, gamma2_max, tau_min, tau_max]

im = plt.imshow(
    mean_hitting_times, 
    extent=extent, 
    origin='lower', 
    aspect='auto', 
    cmap='viridis_r'
)

fonts = 12
plt.xlabel(rf'$\gamma_2$', fontsize = fonts)
plt.ylabel(rf'$\tau$', fontsize = fonts)
plt.title(rf'$N = {num_sites}$, $i={target_site}$, $\phi_1 = {phi_1}$', fontsize = fonts)

ticks = [0.0, 0.25, 0.5, 0.75, 1.0]
tick_labels = [rf'$0.0$', rf'$0.25$', rf'$0.50$', rf'$0.75$', rf'$1.0$']
plt.xticks(ticks, tick_labels)

cbar = plt.colorbar(im)
filename = rf'CPLUSPLUS_gamma2_vs_tau_mean_hitting_time_PVM_phi1_{phi_1}_N_{num_sites}_resolution_{num_gamma2_points}x{num_tau_points}'
plt.savefig(filename+'.pdf')
plt.show()